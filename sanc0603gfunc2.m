G=cos(sita);
S=sin(sita);
C11=C11y*G^4+C22y*S^4+2*(C12y+2*C66y)*(S^2)*(G^2);
C12=(C11y+C22y-4*C66y)*(S^2)*(G^2)+C12y*((S^4)+(G^4));
C13=C13y*G^2+C23y*S^2;
C16=(C12y+2*C66y-C11y)*S*G^3+(C22y-C12y-2*C66y)*G*S^3;
C22=C11y*S^4+2*(C12y+2*C66y)*(G^2)*(S^2)+C22y*G^4;
C23=C23y*G^2+C13y*S^2;
C26=(C12y+2*C66y-C11y)*G*(S^3)+(C22y-C12y-2*C66y)*S*(G^3);
C33=C33y;
C36=(C23y-C13y)*S*G;
C44=C44y*(G^2)+C55y*(S^2);
C45=(C44y-C55y)*S*G;
C55=C55y*(G^2)+C44y*(S^2);
C66=C66y*(S^4+G^4)+(C11y+C22y-2*C66y)*(S^2)*(G^2);

F111=C11-rou*c^2;
F112=C16;
F113=C13+C55;
F122=C66-rou*c^2;
F123=C36+C45;
F133=C55-rou*c^2;

p11=C44*C33;
p12=F122*C33+F133*C44-F123^2;
p13=F122*F133;
